
public class Operator2 { // 비교연산자

	public static void main(String[] args) {
		int i = 10; // '=' 단순 대입 연산자
		int j = 10; 
		
		System.out.println(i==j); // true
		System.out.println(i!=j); // false
		System.out.println(i<j); // false
		System.out.println(i<=j); // true
		
		// 복합 대입 연산자 += -= *= %= /=
		i += 10; // i = i + 10 
	}

}
